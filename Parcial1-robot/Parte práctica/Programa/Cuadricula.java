package parcial1;

public class Cuadricula {

	int cantFilas;
	int cantColumnas;
	
	public Cuadricula(int cantFilas, int cantColumnas) {
		this.cantFilas = cantFilas;
		this.cantColumnas = cantColumnas;
	}

	public int getCantFilas() {
		return cantFilas;
	}

	public int getCantColumnas() {
		return cantColumnas;
	}
	
}
